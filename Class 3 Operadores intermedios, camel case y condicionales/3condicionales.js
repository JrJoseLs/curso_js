//if - solo al 1ro, else if - podemos poner infinitos, else - solo ultimo.

// validar 2 cosas, se puede usar 2 if.

nombre = "pedro";

// si la condicion se cumple arroja el resultado
if (nombre == "Juan") {
    alert("tu nombre es " + nombre);
}
// si la condicion anterior no se cumple cambia a este resultado
else if (nombre == "slow"){
    alert("tu nombre es " + nombre);
}
// si ninguna se cumple...
else {
    alert ("tu nombre es otro");
}